package MODEL;

public class Entrada {

    int idEntrada, idProdutoEntrada, quantEntrada;
    String dataEntrada;
    double valorCompraEntrada;
    int idFornecedorEntrada, idUsuarioEntrada;

    public int getIdEntrada() {
        return idEntrada;
    }

    public void setIdEntrada(int idEntrada) {
        this.idEntrada = idEntrada;
    }

    public int getIdProdutoEntrada() {
        return idProdutoEntrada;
    }

    public void setIdProdutoEntrada(int idProdutoEntrada) {
        this.idProdutoEntrada = idProdutoEntrada;
    }

    public int getQuantEntrada() {
        return quantEntrada;
    }

    public void setQuantEntrada(int quantEntrada) {
        this.quantEntrada = quantEntrada;
    }

    public String getDataEntrada() {
        return dataEntrada;
    }

    public void setDataEntrada(String dataEntrada) {
        this.dataEntrada = dataEntrada;
    }

    public double getValorCompraEntrada() {
        return valorCompraEntrada;
    }

    public void setValorCompraEntrada(double valorCompraEntrada) {
        this.valorCompraEntrada = valorCompraEntrada;
    }

    public int getIdFornecedorEntrada() {
        return idFornecedorEntrada;
    }

    public void setIdFornecedorEntrada(int idFornecedorEntrada) {
        this.idFornecedorEntrada = idFornecedorEntrada;
    }

    public int getIdUsuarioEntrada() {
        return idUsuarioEntrada;
    }

    public void setIdUsuarioEntrada(int idUsuarioEntrada) {
        this.idUsuarioEntrada = idUsuarioEntrada;
    }
}
