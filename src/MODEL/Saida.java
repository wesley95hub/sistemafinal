package MODEL;

public class Saida {

    int idsaida;
    String qtdSaida, dataSaida;
    int idProdutoSaida, idUsuarioSaida;

    public int getIdsaida() {
        return idsaida;
    }

    public void setIdsaida(int idsaida) {
        this.idsaida = idsaida;
    }

    public String getQtdSaida() {
        return qtdSaida;
    }

    public void setQtdSaida(String qtdSaida) {
        this.qtdSaida = qtdSaida;
    }

    public String getDataSaida() {
        return dataSaida;
    }

    public void setDataSaida(String dataSaida) {
        this.dataSaida = dataSaida;
    }

    public int getIdProdutoSaida() {
        return idProdutoSaida;
    }

    public void setIdProdutoSaida(int idProdutoSaida) {
        this.idProdutoSaida = idProdutoSaida;
    }

    public int getIdUsuarioSaida() {
        return idUsuarioSaida;
    }

    public void setIdUsuarioSaida(int idUsuarioSaida) {
        this.idUsuarioSaida = idUsuarioSaida;
    }
}
