    private void JBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAlterarActionPerformed
            int opcao = JOptionPane.showConfirmDialog(null,"Deseja realmente artualizar os dados da cargo","Mensagem do sistema",JOptionPane.INFORMATION_MESSAGE);
            if (opcao == 0) {
                controller.alterar(this.criarObjeto());
                limparCampo();
                habilitarCampos(3);
            }
    }//GEN-LAST:event_JBAlterarActionPerformed

    private void JBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBExcluirActionPerformed
        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir a Cargo?", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
        if (opcao == 0) {
            controller.deletar(this.criarObjeto());
            TbCargos.removeRow(JTCargo.getSelectedRow());
            this.listarCargo();
        }


    }//GEN-LAST:event_JBExcluirActionPerformed
    public Cargo criarObjeto() {
        car = new Cargo();

        car.setNomeCargo(String.valueOf(JTCargo.getValueAt(JTCargo.getSelectedRow(), 1)));
        car.setIdCargo(Integer.parseInt(JTCargo.getValueAt(JTCargo.getSelectedRow(), 0).toString()));
        return car;
    }
