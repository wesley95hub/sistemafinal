    private void listarCargo() {
        cargos = controller.selecionarCargos();
        this.removerLinha();
        for (Cargo car : cargos) {
            TbCargos.addRow(new Object[]{car.getIdCargo(), car.getNomeCargo()});
        }
    }

    private void removerLinha() {
        int linhas = TbCargos.getRowCount();
        for (int i = linhas - 1; i >= 0; i--) {
            TbCargos.removeRow(i);
        }
    }

    public void pesquisar() {
        cargos = controller.pesquisarCargos(JTFPesquisar.getText());
        if (cargos.isEmpty()) {
            this.limparCampo();
            this.habilitarCampos(3);
            JOptionPane.showMessageDialog(null, "Cargo não encontrada", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
        } else {
            this.removerLinha();
            for (Cargo car : cargos) {
                TbCargos.addRow(new Object[]{car.getIdCargo(), car.getNomeCargo()});
            }
            this.habilitarCampos(1);
        }
    }

    private void limparCampo() {
        JTFPesquisar.setText("");
    }

    private void habilitarCampos(int i) {
        switch (i) {
            case 1://Pesquisar
                JBNovo.setEnabled(false);
                JBAlterar.setEnabled(true);
                JBExcluir.setEnabled(true);
                JBCancelar.setEnabled(true);
                break;
            case 2://cancelar
                JBNovo.setEnabled(true);
                JBAlterar.setEnabled(false);
                JBExcluir.setEnabled(false);
                JBCancelar.setEnabled(false);
                JBPesquisar.setEnabled(true);
                JTFPesquisar.setEnabled(true);
                break;
            case 3: //salvar, excluir, alterar
                JBPesquisar.setEnabled(true);
                JTFPesquisar.setEnabled(true);
                JBNovo.setEnabled(true);
                JBAlterar.setEnabled(false);
                JBExcluir.setEnabled(false);
                JBCancelar.setEnabled(false);
                break;
            default:
                break;

        }
    }
