    private void listarCliente() {
       clientes = controller.selecionarClientes();
        this.removerLinha();
        for (Cliente cli : clientes) {
               TbCliente.addRow(new Object[]{
                cli.getCodigo(),
                cli.getNome(),
                cli.getRg(),
                cli.getCpf(),              
                cli.getEmail(),
                cli.getDataNasc(),
                cli.getTelefone(),
                cli.getCelular(),
            });
    }
  }

    private void removerLinha() {
        int linhas = TbCliente.getRowCount();
        for (int i = linhas - 1; i >= 0; i--) {
            TbCliente.removeRow(i);
        }
    }

    public void pesquisar() {
        clientes = controller.pesquisarClientes(JTFPesquisar.getText());

        if (clientes.isEmpty()) {
            limparCampo();
            this.habilitarCampos(3);
            JOptionPane.showMessageDialog(null, "Cliente não encontrada", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
        } else {
            this.removerLinha();
            for (Cliente cli : clientes) {
                TbCliente.addRow(new Object[]{
                    cli.getCodigo(),
                    cli.getNome(),
                    cli.getRg(),
                    cli.getCpf(),
                    cli.getEmail(),
                    cli.getDataNasc(),
                    cli.getTelefone(),
                    cli.getCelular(),
                });
            }
        }
            this.habilitarCampos(1);
       
    }

    private void limparCampo() {
        JTFPesquisar.setText("");
    }

    private void habilitarCampos(int i) {
        switch (i) {
            case 1://Pesquisar
                JBNovo.setEnabled(false);
                JBAlterar.setEnabled(true);
                JBExcluir.setEnabled(true);
                JBCancelar.setEnabled(true);
                break;
            case 2://cancelar
                JBNovo.setEnabled(true);
                JBAlterar.setEnabled(false);
                JBExcluir.setEnabled(false);
                JBCancelar.setEnabled(false);
                JBPesquisar.setEnabled(true);
                JTFPesquisar.setEnabled(true);
                break;
            case 3: //salvar, excluir, alterar
                JBPesquisar.setEnabled(true);
                JTFPesquisar.setEnabled(true);
                JBNovo.setEnabled(true);
                JBAlterar.setEnabled(false);
                JBExcluir.setEnabled(false);
                JBCancelar.setEnabled(false);
                break;
            default:
                break;

        }
    }
