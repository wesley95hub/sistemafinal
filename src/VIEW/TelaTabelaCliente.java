    private void JBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAlterarActionPerformed
        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente artualizar os dados da Cliente", "Mensagem do sistema", JOptionPane.INFORMATION_MESSAGE);
        if (opcao == 0) {
            controller.alterar(this.criarObjeto());
            this.limparCampo();
            habilitarCampos(3);
        }
    }//GEN-LAST:event_JBAlterarActionPerformed

    private void JBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBExcluirActionPerformed
        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir a Cliente?", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
        if (opcao == 0) {            
            this.criarObjeto();
            EnderecoController  enderecoController = new EnderecoController();
            Endereco end = new Endereco();
            end = enderecoController.pesquisarEnderecosCliente(cli.getCodigo());
            enderecoController.deletar(end);
            controller.deletar(cli);
            TbCliente.removeRow(JTCliente.getSelectedRow());
        }
        this.habilitarCampos(3);

    }//GEN-LAST:event_JBExcluirActionPerformed
    public Cliente criarObjeto() {
        cli.setCodigo(Integer.valueOf(String.valueOf(JTCliente.getValueAt(JTCliente.getSelectedRow(), 0))));
        cli.setNome(String.valueOf(JTCliente.getValueAt(JTCliente.getSelectedRow(), 1)));
        cli.setRg(String.valueOf(JTCliente.getValueAt(JTCliente.getSelectedRow(), 2)));
        cli.setCpf(String.valueOf(JTCliente.getValueAt(JTCliente.getSelectedRow(), 3)));
        cli.setEmail(String.valueOf(JTCliente.getValueAt(JTCliente.getSelectedRow(), 4))); 
        cli.setDataNasc(String.valueOf(JTCliente.getValueAt(JTCliente.getSelectedRow(), 5)));
        cli.setTelefone(String.valueOf(JTCliente.getValueAt(JTCliente.getSelectedRow(), 6)));
        cli.setCelular(String.valueOf(JTCliente.getValueAt(JTCliente.getSelectedRow(), 7)));
        return cli;
    }
