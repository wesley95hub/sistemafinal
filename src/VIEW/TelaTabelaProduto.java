    private void JBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAlterarActionPerformed
        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente artualizar os dados da produto", "Mensagem do sistema", JOptionPane.INFORMATION_MESSAGE);
        if (opcao == 0) {
           controllerProduto.alterar(this.criarObjeto());
            limparCampo();
            habilitarCampos(3);
        }
    }//GEN-LAST:event_JBAlterarActionPerformed

    private void JBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBExcluirActionPerformed
        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir a Produto?", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
        if (opcao == 0) {
            controllerProduto.deletar(this.criarObjeto());
            TbProdutos.removeRow(JTProduto.getSelectedRow());
        }


    }//GEN-LAST:event_JBExcluirActionPerformed
    public Produto criarObjeto() {
        prod = new Produto();
        prod = controllerProduto.pesquisar(Integer.parseInt(JTProduto.getValueAt(JTProduto.getSelectedRow(), 0).toString()));
        prod.setNomeProduto(String.valueOf(JTProduto.getValueAt(JTProduto.getSelectedRow(), 1)));
        prod.setModeloProduto(String.valueOf(JTProduto.getValueAt(JTProduto.getSelectedRow(), 2)));
        prod.setDataVencimentoProduto(String.valueOf(JTProduto.getValueAt(JTProduto.getSelectedRow(), 5)));
        prod.setDescricaoProduto(String.valueOf(JTProduto.getValueAt(JTProduto.getSelectedRow(), 6)));
        prod.setPercetualProduto(Double.parseDouble(JTProduto.getValueAt(JTProduto.getSelectedRow(), 7).toString()));
        prod.setPesoProduto(Double.parseDouble(JTProduto.getValueAt(JTProduto.getSelectedRow(), 8).toString()));
        prod.setUnidCompraProduto(String.valueOf(JTProduto.getValueAt(JTProduto.getSelectedRow(), 9)));
        prod.setUnidVendaProduto(String.valueOf(JTProduto.getValueAt(JTProduto.getSelectedRow(), 10)));
        prod.setValorVendaProduto(Double.parseDouble(JTProduto.getValueAt(JTProduto.getSelectedRow(), 11).toString()));
        return prod;
    }
