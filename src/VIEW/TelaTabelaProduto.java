    private void listarProduto() {
        produtos = controllerProduto.selecionarProdutos();
        this.removerLinha();
        for (Produto prod : produtos) {
            TbProdutos.addRow(new Object[]{
                prod.getIdProduto(),
                prod.getNomeProduto(),
                prod.getModeloProduto(),
                ControllerMarca.pesquisar(prod.getIdMarcaProduto()).getNomeMarca(),
                ControllerTipo.pesquisar(prod.getIdTipoProduto()).getNomeTipo(),
                prod.getDataVencimentoProduto(),
                prod.getDescricaoProduto(),
                prod.getPercetualProduto(),
                prod.getPesoProduto(),
                prod.getUnidCompraProduto(),
                prod.getUnidVendaProduto(),
                prod.getValorVendaProduto(),    
                
            });
        }
    }

    ;

    private void removerLinha() {
        int linhas = TbProdutos.getRowCount();
        for (int i = linhas - 1; i >= 0; i--) {
            TbProdutos.removeRow(i);
        }
    }

    public void pesquisar() {
        produtos = controllerProduto.pesquisarProdutos(JTFPesquisar.getText());
        if (produtos.isEmpty()) {
            this.limparCampo();
            this.habilitarCampos(3);
            JOptionPane.showMessageDialog(null, "Produto não encontrada", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
        } else {
            this.removerLinha();
            for (Produto prod : produtos) {
                TbProdutos.addRow(new Object[]{ prod.getIdProduto(),
                prod.getNomeProduto(),
                prod.getModeloProduto(),
                ControllerMarca.pesquisar(prod.getIdMarcaProduto()).getNomeMarca(),
                ControllerTipo.pesquisar(prod.getIdTipoProduto()).getNomeTipo(),
                prod.getDataVencimentoProduto(),
                prod.getDescricaoProduto(),
                prod.getPercetualProduto(),
                prod.getPesoProduto(),
                prod.getUnidCompraProduto(),
                prod.getUnidVendaProduto(),
                prod.getValorVendaProduto()});
            }
            this.habilitarCampos(1);
        }
    }

    private void limparCampo() {
        JTFPesquisar.setText("");
    }

    private void habilitarCampos(int i) {
        switch (i) {
            case 1://Pesquisar
                JBNovo.setEnabled(false);
                JBAlterar.setEnabled(true);
                JBExcluir.setEnabled(true);
                JBCancelar.setEnabled(true);
                break;
            case 2://novo
                JBNovo.setEnabled(true);
                JBAlterar.setEnabled(false);
                JBExcluir.setEnabled(false);
                JBCancelar.setEnabled(false);
                JBPesquisar.setEnabled(true);
                JTFPesquisar.setEnabled(true);
                break;
            case 3: //salvar, excluir, alterar
                JBPesquisar.setEnabled(true);
                JTFPesquisar.setEnabled(true);
                JBNovo.setEnabled(true);
                JBAlterar.setEnabled(false);
                JBExcluir.setEnabled(false);
                JBCancelar.setEnabled(false);
                break;
            default:
                break;

        }
    }
